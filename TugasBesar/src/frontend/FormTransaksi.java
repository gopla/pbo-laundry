    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        if (Integer.parseInt(bayar.getText()) < Integer.parseInt(total_harga.getText())) {
            JOptionPane.showMessageDialog(rootPane, "Uang Kurang");
        } else {
            JOptionPane.showMessageDialog(rootPane, "Data Dimasukkan");
            Transaksi trans = new Transaksi();
            trans.setNama(nama.getText());
            trans.setId_user(Integer.parseInt(id_userHidden.getText()));
            trans.setBerat(Integer.parseInt(berat.getText()));
            trans.setJenisCuci(jenis_cuci.getSelectedItem().toString());
            trans.setTotal(Integer.parseInt(total_harga.getText()));
            trans.save();
            resetForm();
            this.dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

