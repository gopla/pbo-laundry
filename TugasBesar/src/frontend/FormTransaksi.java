    public void setTeksTotalHarga(){
        berat.addKeyListener(new KeyAdapter() {
            @Override
            public void keyPressed(KeyEvent e){
                if (berat.getText().equals("")) {
                    
                }else{
                    int hargaTotal = Integer.valueOf(harga.getText()) * Integer.valueOf(berat.getText());
                    total_harga.setText(String.valueOf(hargaTotal));
                }
            }
        });
    }
    
    public void setTeksHarga(){
        jenis_cuci.addActionListener(new ActionListener(){
            @Override
            public void actionPerformed(ActionEvent e) {
                if (jenis_cuci.getSelectedItem().toString() == "Setrika") {
                    harga.setText("5000");
                } else if(jenis_cuci.getSelectedItem().toString() == "Cuci Kering"){
                    harga.setText("10000");
                }else if(jenis_cuci.getSelectedItem().toString() == "Cuci Kering Setrika"){
                    harga.setText("15000");
                }
            }
        });
    }

