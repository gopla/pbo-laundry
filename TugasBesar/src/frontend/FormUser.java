    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        JOptionPane.showMessageDialog(rootPane, "Data Dimasukkan");
        Kasir user = new Kasir();
        user.setNama(txtNama.getText());
        user.setUsername(txtUname.getText());
        user.setPassword(txtPass.getText());
        user.setRole(cmbLevel.getSelectedItem().toString());
        if (cmbLevel.getSelectedItem().toString() == "Kasir") {
            user.setGaji(Integer.parseInt(txtGaji.getText()));
        } else {
            user.setGaji(0);
        }
        user.save();
        tampilkanData();
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)tblUser.getModel();
        int row = tblUser.getSelectedRow();
        
        Kasir kasir = new Kasir().getById(Integer.parseInt(model.getValueAt(row, 0).toString()));
        kasir.delete();
        resetForm();
        tampilkanData();
    }//GEN-LAST:event_btnHapusActionPerformed

