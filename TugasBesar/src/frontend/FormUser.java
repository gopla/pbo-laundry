    public void hideTeksGaji(){
        cmbLevel.addActionListener(new ActionListener(){
            @Override
            public void actionPerformed(ActionEvent e) {
                if (cmbLevel.getSelectedItem().toString() == "Admin") {
                    txtGaji.setVisible(false);
                    jLabel6.setVisible(false);
                }else{
                    txtGaji.setVisible(true);
                    jLabel6.setVisible(true);
                }
            }
            
        });
    }
    
    public void resetForm(){
        txtNama.setText("");
        txtUname.setText("");
        txtPass.setText("");
        txtGaji.setText("");
        cmbLevel.setSelectedItem("Kasir");
        txtGaji.setVisible(true);
        jLabel6.setVisible(true);
    }
    
    public void tampilkanData() {
        String[] kolom = {"ID", "Nama", "Gaji", "Username", "Password", "Role"};
        ArrayList<Kasir> list = new Kasir().getAll();
        Object rowData[] = new Object[6];
        
        tblUser.setModel(new DefaultTableModel(new Object[][] {}, kolom));
        
        for (int i = 0; i < list.size(); i++) {
            rowData[0] = list.get(i).getId_user();
            rowData[1] = list.get(i).getNama();
            rowData[2] = list.get(i).getGaji();
            rowData[3] = list.get(i).getUsername();
            rowData[4] = list.get(i).getPassword();
            rowData[5] = list.get(i).getRole();
            
            ((DefaultTableModel)tblUser.getModel()).addRow(rowData);
        }
    }

